package blogpost;

public class BlogPost {
    String authorName;
    String text;
    String title;
    String publicationDate;

    public BlogPost(String authorName, String text, String title, String publicationDate) {
        this.authorName = authorName;
        this.text = text;
        this.title = title;
        this.publicationDate = publicationDate;
    }
}
